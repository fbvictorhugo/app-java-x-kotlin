package net.fbvictorhugo.j.barreirasanitaria.modelo;

import java.util.Date;

public class Pessoa {

    private long id;
    private String nome;
    private long numeroDocumeto;
    private int tipoDocumento;
    private Date dataNascimento;
    private int telefone;
    private String cidade;
    private String estado;
    private String bairro;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public long getNumeroDocumeto() {
        return numeroDocumeto;
    }

    public void setNumeroDocumeto(long numeroDocumeto) {
        this.numeroDocumeto = numeroDocumeto;
    }

    public int getTipoDocumento() {
        return tipoDocumento;
    }

    public void setTipoDocumento(int tipoDocumento) {
        this.tipoDocumento = tipoDocumento;
    }

    public Date getDataNascimento() {
        return dataNascimento;
    }

    public void setDataNascimento(Date dataNascimento) {
        this.dataNascimento = dataNascimento;
    }

    public int getTelefone() {
        return telefone;
    }

    public void setTelefone(int telefone) {
        this.telefone = telefone;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getBairro() {
        return bairro;
    }

    public void setBairro(String bairro) {
        this.bairro = bairro;
    }
}
